def rewrite_personas():
    for name, config in REWRITES.items():
        path = PERSONA_DIR / f"{name}.yaml"
        data = yaml.safe_load(open(path, encoding="utf-8"))
        data["system_prompt_template"] = config["system_prompt_template"]
        data["workflow"] = config["workflow"]
        data["tool_guidelines"] = config["tool_guidelines"]
        with open(path, "w", encoding="utf-8") as f:
            yaml.dump(data, f, allow_unicode=True, sort_keys=False, default_flow_style=False)
        print(f"  ✅ {name}: system_prompt+workflow+tool_guidelines 已写入")


if __name__ == "__main__":
    rewrite_personas()
